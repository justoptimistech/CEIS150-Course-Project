class InventoryTag:
    def __init__(self):
        self.item_id = 0
        self.quantity_remaining = 0

red_sweater = InventoryTag()
red_sweater.item_id = int(input("What is the item ID: "))
red_sweater.quantity_remaining = int(input("What is the Qty: "))

print('ID:', red_sweater.item_id)
print('Qty:', red_sweater.quantity_remaining)
